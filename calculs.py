def addition(a, b):
    """Cette fonction permet d'additionner deux nombres entiers"""
    return a + b

def soustraction(a, b):
    """Cette fonction permet de faire la diff√©rence deux nombres entiers"""
    return a - b

def multiplication(a, b):
    """Cette fonction permet de multiplier deux nombres entiers"""
    return a * b

def division(a, b):
    """Cette fonction permet de diviser deux nombres entiers"""
    return a / b

def reste_division(a, b):
    """Cette fonction de retourner le reste de la division
    d'un nombre entier par un autre"""
    return a % b
